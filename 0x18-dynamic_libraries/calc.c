/**
 * add - returns the sum of two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: a + b
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - returns the difference between two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: a - b
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - returns the product of two integers
 * @a: first intger
 * @b: second integer
 *
 * Return: a * b
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - returns the result of a / b
 * @a: first integer
 * @b: second integer
 *
 * Return: a / b
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - returns the modulus of dividing a by b (a % b)
 * @a: first integer
 * @b: second integer
 *
 * Return: a % b
 */
int mod(int a, int b)
{
	return (a % b);
}
